/*   DISTANCE CALCULATOR   */
#include<stdio.h>
#include<conio.h>
#define p printf
#define s scanf
void main()
{
  clrscr();
  float km,m,f,i,cm;
  p("\n Enter the distance in Kilometer (km) ::");
  s("%f",&km);
  m=km*1000;
  cm=m*100;
  f=cm/30.48;
  i=cm/2.54;
  p("\n %f km = %f m",km,m);
  p("\n %f km = %f cm",km,cm);
  p("\n %f km = %f feet",km,f);
  p("\n %f km = %f inches",km,i);
  getch();
}
